import { askGigaChat } from "../services/gigachat.js";

/**
 * Модуль 4.2: Анализирует сообщение пассажира
 */
export async function analyzePassengerMessage(text) {
  const prompt = `
    Ты — строгий анализатор данных транспортной компании. Твоя задача только возвращать JSON.
    Проанализируй сообщение пассажира: "${text}"
    
    Верни строго JSON-объект с ключами:
    "isDelayQuestion": true/false (спрашивает ли человек про задержку, ожидание или отсутствие транспорта)
    "transportType": "tram" (трамвай), "trolleybus" (троллейбус), "bus" (автобус) или null
    "route": номер маршрута строкой (например "5", "20А") или null
    "location": название остановки/улицы или null
    "direction": направление движения (например, "в центр", "в депо") или null
    `;

  return await askGigaChat(prompt);
}

/**
 * Модуль 4.1: Анализирует сообщение диспетчера
 */
export async function analyzeDispatcherMessage(text) {
  const prompt = `
    Ты — строгий анализатор заявок диспетчера. Твоя задача — вернуть ТОЛЬКО валидный JSON-объект без форматирования Markdown и без пояснительного текста.
    
    Извлеки данные из сообщения: "${text}"
    
    Схема JSON:
    {
      "incidentType": "обрыв_сети" | "дтп" | "поломка" | "отключение_тока" | "иная_причина",
      "transportType": "tram" | "trolleybus" | "metro",
      "routes": ["номер_маршрута"],
      "location": "строка или null",
      "direction": "строка или null"
    }

    Правила:
    1. Если встретил "аварию" — пиши "дтп".
    2. Маршруты (routes): 
       - Извлекай как массив строк.
       - Убирай символ '№'.
       - Если написано "Трамвай 3 и 4" -> ["3", "4"].
       - Если номер не найден -> [].
    3. Если сообщение не об инциденте — верни пустой объект {}.
    4. НЕ ПИШИ НИКАКОГО ТЕКСТА КРОМЕ JSON.
    `;

  return await askGigaChat(prompt);
}

/**
 * Модуль 4.5: Анализирует сообщение об устранении аварии
 */
export async function analyzeResolutionMessage(text) {
  const prompt = `
    Ты — анализатор заявок на ЗАКРЫТИЕ инцидентов.
    Извлеки данные из сообщения: "${text}"
    
    Верни ТОЛЬКО JSON:
    {
      "transportType": "tram" | "trolleybus" | "metro",
      "routes": ["номер"],
      "location": "адрес или null"
    }

    Правила:
    1. Ищи номер маршрута в любом виде (например, "Трамвай 1", "Трамвай № 1", "единица"). 
    2. Если номер указан, обязательно помести его в массив routes, даже если нет описания проблемы.
    3. Если сообщение содержит адрес/остановку — вытащи её в location.
    4. Если ничего не понятно — верни пустой объект {}.
    `;
  
  const result = await askGigaChat(prompt);
  console.log("🔍 [NLP] Результат парсинга закрытия:", result);
  return result;
}
