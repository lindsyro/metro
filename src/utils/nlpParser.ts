import { askGigaChat } from "../services/gigachat.js";

/**
 * Модуль 4.2: Анализирует сообщение пассажира
 */
export async function analyzePassengerMessage(text: string) {
  const prompt = `
    Ты — строгий анализатор данных транспортной компании. Твоя задача только возвращать JSON.
    Проанализируй сообщение пассажира: "${text}"
    
    Верни строго JSON-объект с ключами:
    {
      "isDelayQuestion": true или false,
      "transportType": "tram" | "trolleybus" | "metro" | null,
      "routes": ["массив", "номеров"] или [],
      "location": "строка или null",
      "direction": "строка или null"
    }

    Правила:
    1. Поле routes должно содержать ТОЛЬКО номера (строками). Если номеров нет, верни пустой массив []. Никогда не пиши туда названия улиц!
    2. Если пользователь называет улицу, район или остановку, пиши это ТОЛЬКО в поле location.
    3. Тип транспорта указывай строго на английском, если он не указан — ставь null.
    `;

  return await askGigaChat(prompt);
}

/**
 * Модуль 4.1: Анализирует сообщение диспетчера
 */
export async function analyzeDispatcherMessage(text: string) {
  const prompt = `
    Ты — строгий анализатор заявок диспетчера. Твоя задача — вернуть ТОЛЬКО валидный JSON-объект без форматирования Markdown и без пояснительного текста.
    
    Извлеки данные из сообщения: "${text}"
    
    Схема JSON:
    {
      "incidentType": "обрыв сети" | "дтп" | "поломка" | "отключение тока" | "иная причина" | null,
      "transportType": "tram" | "trolleybus" | "metro" | null,
      "routes": ["номер_маршрута"] или [],
      "location": "строка или null",
      "direction": "строка или null"
    }

    Правила:
    1. Если встретил "аварию" — пиши "дтп".
    2. Маршруты (routes): 
       - Извлекай как массив строк.
       - Убирай символ '№'.
       - Если написано "Трамвай 3 и 4" -> ["3", "4"].
       - Если номер не найден -> [].
    3. КРИТИЧЕСКИ ВАЖНО: НИКОГДА не пытайся угадать transportType по контексту (контактная сеть, рельсы и т.д.).
       Если в тексте прямо НЕ написано слово "трамвай" (и его производные) или "троллейбус", ОБЯЗАТЕЛЬНО ставь null в поле transportType.
    4. Если сообщение не об инциденте — верни пустой объект {}.
    5. НЕ ПИШИ НИКАКОГО ТЕКСТА КРОМЕ JSON.
    `;

  return await askGigaChat(prompt);
}

/**
 * Модуль 4.5: Анализирует сообщение об устранении аварии
 */
export async function analyzeResolutionMessage(text: string) {
  const prompt = `
    Ты — анализатор заявок на ЗАКРЫТИЕ инцидентов.
    Извлеки данные из сообщения: "${text}"
    
    Верни ТОЛЬКО JSON:
    {
      "transportType": "tram" | "trolleybus" | "metro" | null,
      "routes": ["номер_маршрута"] или [],
      "location": "строка или null"
    }

    Правила:
    1. Ищи номер маршрута в любом виде (например, "Трамвай 1", "Трамвай № 1", "единица"). 
    2. Если номер указан, обязательно помести его в массив routes, даже если нет описания проблемы. Если номера нет -> [].
    3. КРИТИЧЕСКИ ВАЖНО: Как и в создании заявок, если тип транспорта прямо не назван, возвращай null в поле transportType.
    4. Если сообщение содержит адрес/остановку — вытащи её в location.
    5. Если ничего не понятно — верни пустой объект {}.
    `;

  const result = await askGigaChat(prompt);
  console.log("🔍 [NLP] Результат парсинга закрытия:", result);
  return result;
}
